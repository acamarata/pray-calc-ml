def _parse_date(date_str: str) -> str:
    """Convert DD-MM-YYYY to YYYY-MM-DD."""
    parts = date_str.split("-")
    return f"{parts[2]}-{parts[1]}-{parts[0]}"


def load_precomputed_angles() -> pd.DataFrame:
    """
    Return all pre-computed angle records as a DataFrame ready for pipeline merge.

    Output columns match the pipeline's post-angle-computation format:
    date, utc_dt (placeholder), lat, lng, elevation_m, prayer, angle, source, notes

    Since these records have pre-computed angles (from SQM time-series linear
    fitting), we generate a placeholder utc_dt at 04:00 local time for Fajr
    records. This is only used for day_of_year computation. The angle column
    is the actual measured value and will NOT be overwritten by the pipeline.
    """
    from datetime import timedelta

    rows = []
    for site_key, date_str, angle_abs, weather in _BASTHONI_RAW:
        if site_key not in BASTHONI_SITES:
            continue

        lat, lng, elev = BASTHONI_SITES[site_key]
        utc_offset = SITE_UTC_OFFSETS.get(site_key, 7)
        category = SITE_CATEGORIES.get(site_key, "unknown")
        date_iso = _parse_date(date_str)

        # Generate a placeholder UTC datetime (04:00 local for Fajr)
        # This is only used for day_of_year; the angle is pre-computed.
        local_dt = datetime.strptime(f"{date_iso} 04:00", "%Y-%m-%d %H:%M")
        utc_dt = (local_dt - timedelta(hours=utc_offset)).replace(
            tzinfo=timezone.utc
        )

        weather_en = {
            "CERAH": "clear", "MENDUNG": "cloudy", "RUSAK": "damaged/bad"
        }.get(weather, weather)

        rows.append({
            "date": date_iso,
            "utc_dt": utc_dt,
            "lat": lat,
            "lng": lng,
            "elevation_m": elev,
            "prayer": "fajr",
            "angle": angle_abs,
            "source": f"{BASE_SOURCE}, {site_key.replace('_', ' ').title()}",
            "notes": (
                f"SQM pre-computed angle; {category}; "
                f"weather: {weather_en}; Lampiran appendix data"
            ),
        })

    df = pd.DataFrame(rows)
    if len(df) > 0:
        df["utc_dt"] = pd.to_datetime(df["utc_dt"])
    return df
