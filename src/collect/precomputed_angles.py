"""
Pre-computed depression angle records from sources where the solar depression
angle was measured directly by instrument (SQM time-series + linear fitting)
rather than inferred from a clock time.

These records bypass the pipeline's normal back-calculation step because the
angle was determined by the original researchers from continuous photometric
data. The SQM records the sky brightness every few seconds; the dawn onset
is found by fitting a linear model to the stable nighttime portion and
identifying the inflection point. The solar position at that exact moment
gives the depression angle. This is higher quality than a single human
clock reading because it uses hundreds of data points per night.

Each record has: prayer, date, lat, lng, elevation_m, angle, source, notes.
The pipeline merges these after its own angle computation step.
"""

